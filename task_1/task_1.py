def circular_array_path(n, m):
    path = []
    i = 1  # i - начальный индекс (точка отсчета)

    while True:
        # Добавляем начальный индекс в путь
        path.append(i)

        # Вычисляем следующий индекс
        i = 1 + (i + m - 2) % n

        # Если возвращаемся в начальную точку, завершаем цикл
        if i == 1:
            break

    return path


if __name__ == "__main__":
    while True:
        # Запрашиваем у пользователя значения n и m
        n = input("Окончание массива (n): ")
        m = input("Введите интервал длины (m): ")

        try:
            n = int(n)
            m = int(m)
        except ValueError:
            print("Пожалуйста, введите корректные числовые значения для n и m.")
            continue

        # Вычисляем путь
        path = circular_array_path(n, m)

        # Выводим путь в виде строки без пробелов
        print("Путь:", "".join(map(str, path)))

        # Спрашиваем пользователя, хочет ли он запустить программу снова или выйти
        again = input("Хотите запустить программу снова? (да/нет): ")
        if again.lower() != 'да':
            break

